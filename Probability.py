# Лотерея «Шанс» устроена следующим образом. Имеется N карточек, на
# которых написаны натуральные числа от 1 до N. Участник должен выбрать K различных чисел, записав
# их в свой билет. Затем из карточек случайным образом выбирается K различных. Участник выиграл в
# лотерею, если среди чисел на выбранных карточках хотя бы M совпадает с записанными в билете, иначе
# участник проиграл. Найдите вероятность выигрыша в такую лотерею. Ответ запишите в процентах,
# округлив до 4 знака после запятой.

N = 25
K = 10
M = 6

def binomi(n, k):
    # Функция вычисления биноминального коэффициента
    if k < 0 or k > n:
        return 0
    if k == 0 or k == n:
        return 1
    k = min(k, n - k)
    result = 1
    for i in range(1, k + 1):
        result = result * (n - i + 1) // i
    return result

# Общие исходы
totalout = binomi(N, K)

# Благоприятные исходы
favorable = 0
for j in range(M, K + 1):
    term = binomi(K, j) * binomi(N - K, K - j)
    favorable += term

# Вероятность
probability = (favorable / totalout) * 100
print(f"Вероятность выигрыша: {probability:.4f}%")